package ru.mentor.constant;

/**
 * Перечисление типов менторских тегов.
 * Используется в таблице mentor_tag_def для классификации тегов.
 */
public enum MentorTagType {
    /**
     * Направление преподавания (например, Java, Spring, SQL).
     */
    DIRECTION,

    /**
     * Почётные награды или индивидуальные звания
     * (например, "Лучший учитель года 1998").
     */
    BADGE
}
